var resultadoSub = num_A-num_B;
console.log("Resultado da subtracao entre " +num_A+ " e " +num_B+ " em Js: "+ resultadoSub);

//Multiplicação
var resultadoMult = num_A*num_B;
console.log("Resultado da multiplicacao entre " +num_A+ " e " +num_B+ " em Js: " + resultadoMult);

//Divisão
var resultadoDiv = num_A /num_B;
console.log("Resultado da divisão entre " +num_A+ " e " +num_B+ " em Js: " + resultadoDiv);

// Modulo
var resultadoModulo = num_A%num_B;
